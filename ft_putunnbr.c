#include "libft.h"

void	ft_putunnbr(unsigned int nb)
{
	if (nb >= 10)
	{
		ft_putunnbr(nb / 10);
		nb %= 10;
	}
	if (nb < 10)
		ft_putchar(nb + '0');
}
